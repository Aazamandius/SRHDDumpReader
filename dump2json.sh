#!/bin/bash
TAB=$'\t' 
sed -E -e 's:</color>::g' \
-e 's/<color=[0-9]*,[0-9]*,[0-9]*>//g' \
-e 's/"/\\"/g' \
-e 's/=/": "/g' \
-e 's/^\t*/&"/g' \
-e 's/\r//g' \
-e 's/$/",/' \
-e 's/ \^\{"/": {/g' \
-e 's/"\}"/}/g' \
-e 's/\{,/{/g' \
-e "\$a }" \
-e '1s;^;{ ;' \
$1 | tr '\n' '\f' | sed 's/,\(\f\t*\}\)/\1/g' | tr '\f' '\n'